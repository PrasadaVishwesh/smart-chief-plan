// Default fallback image
export const defaultFallbackImage = "https://images.pexels.com/photos/1640777/pexels-photo-1640777.jpeg?auto=compress&cs=tinysrgb&w=800";

/**
 * Get the best matching image for a recipe
 * Priority: 1. Dish-specific image, 2. Category-based, 3. Cuisine-based, 4. Default
 */
export function getRecipeImage(name: string, cuisine: string): string {
  // 1. Check for exact dish name match
  if (dishSpecificImages[name]) {
    return dishSpecificImages[name];
  }

  // 2. Check for partial name matches in dish-specific images
  const lowerName = name.toLowerCase();
  for (const [dishName, imageUrl] of Object.entries(dishSpecificImages)) {
    if (lowerName.includes(dishName.toLowerCase()) || dishName.toLowerCase().includes(lowerName)) {
      return imageUrl;
    }
  }

  // 3. Check for category-based fallback
  for (const [category, imageUrl] of Object.entries(categoryFallbackImages)) {
    if (lowerName.includes(category)) {
      return imageUrl;
    }
  }

  // 4. Use cuisine-based fallback
  if (cuisineFallbackImages[cuisine]) {
    return cuisineFallbackImages[cuisine];
  }

  // 5. Return default fallback
  return defaultFallbackImage;
}

/**
 * Get fallback image when primary image fails to load
 */
export function getFallbackImage(cuisine: string, name: string): string {
  // First try category-based
  const lowerName = name.toLowerCase();
  for (const [category, imageUrl] of Object.entries(categoryFallbackImages)) {
    if (lowerName.includes(category)) {
      return imageUrl;
    }
  }

  // Then cuisine-based
  return cuisineFallbackImages[cuisine] || defaultFallbackImage;
}
